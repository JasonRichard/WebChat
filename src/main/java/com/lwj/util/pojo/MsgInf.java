package com.lwj.util.pojo;

public class MsgInf {
	private boolean fromWho;
	private String text;
	private int id;
	
	public boolean getFromWho() {
		return fromWho;
	}
	public void setFromWho(boolean fromWho) {
		this.fromWho = fromWho;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
}
